package com.syntax.class02;

public class ModulusOperator {
	public static void main (String[] args) {
		
		float f=12.50f;
		float f1=2.7f;
		
		float result=f/f1;
		System.out.println(result);
		
		int i=12;
		int i2=7;
		
		float result2=i/i2;
		
		System.out.println(result2);
		
		int v=16;
		int w=6;
		
		int mod=v%w;
		System.out.println(mod);
		
	}

}
