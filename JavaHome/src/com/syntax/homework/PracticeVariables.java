package com.syntax.homework;

public class PracticeVariables {

	public static void main(String[] args) {
		// numbers data types--> byte, short, int, long.
		//byte whole numbers from -128 to 127.
		byte variableA=-128;
		//short whole numbers from -32768 to 32767.
		short variableB=-32768;
		//int whole numbers from -2147483648 to 2147483647
		int variableC=-2147483648;
		//long whole numbers from -9223372036854775808 to 9223372036854775807
		long variableD=1234567890;
		
		//numbers data types -->float, double.
		//float and double fractional numbers
		float variableE=33.787878f;
		double variableF=99.45454545454;
		
		//data type single characters--> char.
		//char single characters/letter/ASCII values
		char color='r';
		
		//data type boolean true or false.
		boolean answer=true;
		
		System.out.println(variableA);
		System.out.println(variableB);
		System.out.println(variableC);
		System.out.println(variableD);
		System.out.println(variableE);
		System.out.println(variableF);
		System.out.println(color);
		System.out.println(answer);
			

	}

}
